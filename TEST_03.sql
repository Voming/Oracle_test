DEPT_TITLE, JOB_NAME FROM EMPLOYEE
JOIN DEPARTMENT ON DEPT_CODE = DEPT_ID
JOIN JOB USING (JOB_CODE)
WHERE TO_NUMBER(SUBSTR(EMP_NO, 0,2)) = (SELECT MAX(TO_NUMBER(SUBSTR(EMP_NO, 0,2))) FROM EMPLOYEE);
