SELECT EMP_NAME, EMP_NO, DEPT_TITLE, JOB_NAME FROM EMPLOYEE
JOIN DEPARTMENT ON DEPT_CODE = DEPT_ID
JOIN JOB USING (JOB_CODE)
WHERE TO_NUMBER(SUBSTR(EMP_NO, 0,2)) BETWEEN 77 AND 79 AND 
TO_NUMBER(SUBSTR(EMP_NO, 8,1)) = 2;
